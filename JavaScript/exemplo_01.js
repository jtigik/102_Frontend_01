// Exemplo laço de repetição

let cont = 1;

while (cont <= 5) {
    console.log("While: "+cont);
    cont++;
}
cont = 1;
do {
    console.log("Do-While: "+cont);
    cont++;
} while (cont <= 5);

for (let i = 0; i < i < 10; i++) {
    console.log("For: "+cont);
    
}